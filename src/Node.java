public class Node {

    private int nodeX, nodeY;
    public Node(int nodeX, int nodeY){
        this.nodeX = nodeX;
        this.nodeY = nodeY;
    }

    public int getNodeX() {
        return nodeX;
    }

    public int getNodeY() {
        return nodeY;
    }

    public void setNodeX(int nodeX) {
        this.nodeX = nodeX;
    }

    public void setNodeY(int nodeY) {
        this.nodeY = nodeY;
    }
}
